package com.draft.form;

/**
 * Enum of figure types.
 */
public enum FigureType {

    /**
     * Constant for rectangle.
     */
    RECTANGLE,

    /**
     * Constant for ellipse.
     */
    ELLIPSE,

    /**
     * Constant for line.
     */
    LINE,

    /**
     * Constant for rhombus.
     */
    RHOMBUS,

    /**
     * Constant for triangle.
     */
    TRIANGLE,

    /**
     * Constant for no figure.
     */
    DEFAULT
}
